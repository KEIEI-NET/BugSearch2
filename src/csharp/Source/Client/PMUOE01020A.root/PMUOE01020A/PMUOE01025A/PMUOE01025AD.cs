			/// <summary>
			/// バイト型配列に変換
			/// </summary>
			/// <returns></returns>
			private void FromByteArray(byte[] line)
			{
				_detailMax = 0;
				MemoryStream ms = new MemoryStream();
				ms.Write(line, 0, line.Length);
                ms.Seek(0, SeekOrigin.Begin);

				ms.Read(zaiko.jkbn, 0, zaiko.jkbn.Length); // 情報区分						
				ms.Read(zaiko.seq_no, 0, zaiko.seq_no.Length); // テキストシーケンス番号		
				ms.Read(zaiko.text_len, 0, zaiko.text_len.Length); // テキスト長					
				ms.Read(zaiko.dkbn, 0, zaiko.dkbn.Length); // 電文区分						
				ms.Read(zaiko.kekka, 0, zaiko.kekka.Length); // 処理結果						
				ms.Read(zaiko.tokbn, 0, zaiko.tokbn.Length); // 問合せ／応答区分				
				ms.Read(zaiko.g_id, 0, zaiko.g_id.Length); // 業務ＩＤ						
				ms.Read(zaiko.g_pass, 0, zaiko.g_pass.Length); // 業務パスワード				
				ms.Read(zaiko.prog_ver, 0, zaiko.prog_ver.Length); // 端末プログラムバージョン番号	
				ms.Read(zaiko.kkbn, 0, zaiko.kkbn.Length); // 継続区分						
				ms.Read(zaiko.h_id, 0, zaiko.h_id.Length); // 取引ＩＤ						
				ms.Read(zaiko.ext, 0, zaiko.ext.Length); // 拡張エリア					
				ms.Read(zaiko.gsk, 0, zaiko.gsk.Length); // 業務処理結果					
				ms.Read(zaiko.gsf, 0, zaiko.gsf.Length); // 業務継続フラグ				
				ms.Read(zaiko.seq, 0, zaiko.seq.Length); // シーケンスＮＯ				
				ms.Read(zaiko.bymd, 0, zaiko.bymd.Length); // 端末入力日付・時間			
				ms.Read(zaiko.ymdhms, 0, zaiko.ymdhms.Length); // ホスト日付・時間				
				

				for(int i=0; i < zaiko.zdata.Length; i++)
				{
					ZDATA Zdata = zaiko.zdata[i];
				
					ms.Read(Zdata.khb, 0, Zdata.khb.Length); // 部品番号						
					ms.Read(Zdata.knm, 0, Zdata.knm.Length); // 部品名						
					ms.Read(Zdata.tkhb, 0, Zdata.tkhb.Length); // 問合せ部品番号				
					ms.Read(Zdata.gokan1, 0, Zdata.gokan1.Length); // 互換性コード					
					ms.Read(Zdata.akk, 0, Zdata.akk.Length); // 仕切							
					ms.Read(Zdata.lp, 0, Zdata.lp.Length); // 希望小売り価格				

					// 拠点情報	(1)〜(8)
					for(int j=0; j < Zdata.zaiz.Length; j++)
					{
						ZAIZ Zaiz = zaiko.zdata[i].zaiz[j];

						ms.Read(Zaiz.kcd, 0, Zaiz.kcd.Length); // 拠点コード					
						ms.Read(Zaiz.zsu, 0, Zaiz.zsu.Length); // 在庫数						
					}

					ms.Read(Zdata.lemsg, 0,  Zdata.lemsg.Length);// コメント
                }

                ms.Close();
			}
