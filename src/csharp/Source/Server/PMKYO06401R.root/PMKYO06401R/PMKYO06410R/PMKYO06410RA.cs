            DeleteProc(dcSecInfoSetWork, ref sqlConnection, ref sqlTransaction, ref sqlCommand);
        }
        /// <summary>
        ///  拠点情報設定マスタデータ削除
        /// </summary>
        /// <param name="dcSecInfoSetWork">拠点情報設定マスタデータ</param>
        /// <param name="sqlConnection">データベース接続情報</param>
