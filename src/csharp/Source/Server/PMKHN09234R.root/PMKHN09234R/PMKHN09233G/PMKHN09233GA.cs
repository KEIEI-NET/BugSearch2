    public class MediationBLCodeGuideDB
    {
        /// <summary>
        /// BLCodeGuideDB仲介クラスコンストラクタ
        /// </summary>
        /// <remarks>
        /// <br>Note       : 特にコンストラクタ内の処理は無し。</br>
