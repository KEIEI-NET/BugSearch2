    /// <remarks>
    /// <br>Note       : このクラスはIUpdHisDspDBクラスオブジェクトをGetObjectで戻します。</br>
    /// <br>			完全スタンドアロンにする場合にはこのクラスで直接UpdHisDspDBを</br>
    /// <br>			インスタンス化して戻します。</br>
    /// <br>Programmer : 20081</br>
    /// <br>Date       : 2008.08.11</br>
    /// <br></br>
    /// <br>Update Note: </br>
    /// </remarks>
    public class MediationUpdHisDspDB
    {
        /// <summary>
        /// UpdHisDspDB仲介クラスコンストラクタ
        /// </summary>
        /// <remarks>
        /// <br>Note       : 特にコンストラクタ内の処理は無し。</br>
        /// <br>Programmer : 20081</br>
        /// <br>Date       : 2008.08.11</br>
        /// </remarks>
        public MediationUpdHisDspDB()
        {
        }
        /// <summary>
        /// IUpdHisDspDBインターフェース取得
        /// </summary>
        /// <returns>IUpdHisDspDBオブジェクト</returns>
        public static IUpdHisDspDB GetUpdHisDspDB()
        {
            //USERデータアプリケーションサーバーのPathを取得（提供データAPサーバーの場合には引数を変える）
            string wkStr = LoginInfoAcquisition.GetAPServiceTargetDomain(ConstantManagement_SF_PRO.ServerCode_UserAP);
#if DEBUG
            wkStr = "http://localhost:9001";
#endif
            //AppSettingsからの取得は行わず自分で引数文字列を生成する
            return (IUpdHisDspDB)Activator.GetObject(typeof(IUpdHisDspDB), string.Format("{0}/MyAppUpdHisDsp", wkStr));
        }
    }
}
