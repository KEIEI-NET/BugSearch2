namespace Broadleaf.Application.Remoting.Adapter
{
	/// <summary>
	/// InventoryExtDB仲介クラス
	/// </summary>
	/// <remarks>
	/// <br>Note       : このクラスはIInventoryExtDBクラスオブジェクトをGetObjectで戻します。</br>
	/// <br>			完全スタンドアロンにする場合にはこのクラスで直接InventoryExtDBを</br>
	/// <br>			インスタンス化して戻します。</br>
	/// <br>Programmer : 22035 三橋 弘憲</br>
	/// <br>Date       : 2007.04.04</br>
	/// <br></br>
	/// <br>Update Note: </br>
	/// </remarks>
	public class MediationInventoryExtDB
	{
		/// <summary>
		/// InventoryExtDB仲介クラスコンストラクタ
		/// </summary>
		/// <remarks>
		/// <br>Note       : 特にコンストラクタ内の処理は無し。</br>
		/// <br>Programmer : 22035 三橋 弘憲</br>
		/// <br>Date       : 2007.04.04</br>
		/// </remarks>
		public MediationInventoryExtDB()
		{
		}
		/// <summary>
		/// IInventoryExtDBインターフェース取得
		/// </summary>
		/// <returns>IInventoryExtDBオブジェクト</returns>
        /// <br>Note       : IPrtsInvemtSearchDBインターフェースを取得します。</br>
        /// <br>Programmer : 22035 三橋 弘憲　  </br>
        /// <br>Date       : 2007.04.04</br>
		public static IInventoryExtDB GetInventoryExtDB()
		{
            //USERデータアプリケーションサーバーのPathを取得（提供データAPサーバーの場合には引数を変える）
            string wkStr = LoginInfoAcquisition.GetAPServiceTargetDomain(ConstantManagement_SF_PRO.ServerCode_UserAP);
#if DEBUG
            wkStr = "http://localhost:9001";
#endif
            //AppSettingsからの取得は行わず自分で引数文字列を生成する
            return (IInventoryExtDB)Activator.GetObject(typeof(IInventoryExtDB),string.Format("{0}/MyAppInventoryExt",wkStr));

        }
	}
}
