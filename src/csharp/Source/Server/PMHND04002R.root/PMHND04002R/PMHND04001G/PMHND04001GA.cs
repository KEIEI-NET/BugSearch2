using System;

using Broadleaf.Application.Common;
using Broadleaf.Application.Remoting;
using Broadleaf.Application.Resources;

namespace Broadleaf.Application.Remoting.Adapter
{
    /// <summary>
    /// 検品対象取得DB仲介クラス
    /// </summary>
    /// <remarks>
    /// <br>Note       : このクラスはIHandyInspectDBクラスオブジェクトをGetObjectで戻します。</br>
    /// <br>			 完全スタンドアロンにする場合にはこのクラスで直接HandyInspectDBを</br>
    /// <br>			 インスタンス化して戻します。</br>
    /// <br>Programmer : 朱宝軍</br>
    /// <br>Date       : 2017/06/12</br>
    /// <br></br>
    /// <br>Update Note: </br>
    /// </remarks>
    public class MediationHandyInspectDB
    {
        /// <summary>
        /// HandyInspectDB仲介クラスコンストラクタ
        /// </summary>
        /// <remarks>
        /// <br>Note       : 特にコンストラクタ内の処理は無し。</br>
        /// <br>Programmer : 朱宝軍</br>
        /// <br>Date       : 2017/06/12</br>
        /// </remarks>
        public MediationHandyInspectDB()
        {
        }

        /// <summary>
        /// IHandyInspectDBインターフェース取得
        /// </summary>
        /// <returns>IHandyInspectDBオブジェクト</returns>
        /// <remarks>
        /// <br>Note       : IHandyInspectDBインターフェースを取得します。</br>
        /// <br>Programmer : 朱宝軍</br>
        /// <br>Date       : 2017/06/12</br>
        /// </remarks>
        public static IHandyInspectDB GetHandyInspectDB()
        {
            //USERデータアプリケーションサーバーのPathを取得（提供データAPサーバーの場合には引数を変える）
            string wkStr = LoginInfoAcquisition.GetAPServiceTargetDomain(ConstantManagement_SF_PRO.ServerCode_UserAP);

#if DEBUG
            wkStr = "http://localhost:8008";
#endif

            //AppSettingsからの取得は行わず自分で引数文字列を生成する
            return (IHandyInspectDB)Activator.GetObject(typeof(IHandyInspectDB), string.Format("{0}/MyAppHandyInspect", wkStr));
        }
    }
}
